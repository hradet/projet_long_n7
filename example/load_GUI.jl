# Load parameters from GUI
function loadGUI()
    # Directory path
    dirPath=pwd()

    #---------------------------------------------------------------------------
    #                          Global parameters
    #---------------------------------------------------------------------------
    parameters = (
    Δh = 1, # timestep in hours
    H = 8760, # horizon in hours
    τ = 0.045, # discount rate
    )

    #---------------------------------------------------------------------------
    #                          Data loading from CSV
    #---------------------------------------------------------------------------
    data_op = CSV.read(joinpath(dirPath,"data\\data_op.csv"))

    #---------------------------------------------------------------------------
    #                                  Scenarios
    #---------------------------------------------------------------------------
    scenarios = (
    ld = data_op.ld,
    pv = data_op.pv,
    c_grid_out = data_op.c_grid_out,
    c_grid_in = data_op.c_grid_in,
    )

    #---------------------------------------------------------------------------
    #                                  Load
    #---------------------------------------------------------------------------
    ld = (
    )

    #---------------------------------------------------------------------------
    #                                  Solar panels
    #---------------------------------------------------------------------------
    pv = (
    peakPower = 60,
    )

    #---------------------------------------------------------------------------
    #                                  Li-ion
    #---------------------------------------------------------------------------
    liion = (
    Erated = 100,
    α_p_ch = 1.5,
    α_p_dch = 1.5,
    η_ch = 0.8,
    η_dch = 0.8,
    η_self = 0.,
    α_soc_min = 0.2,
    α_soc_max = 0.8,
    soc_ini = 0.5,
    # Aging
    soh_ini=1.,
    nCycle = 2500,
    dod = 0.6,
    )

    #---------------------------------------------------------------------------
    #                                  Grid
    #---------------------------------------------------------------------------
    grid = (
    powerMax = 36.,
    )

    #---------------------------------------------------------------------------
    #                                  Output
    #---------------------------------------------------------------------------
    outputGUI = Dict(
    "ld" => ld,
    "pv" => pv,
    "liion" => liion,
    "grid" => grid,
    "scenarios" => scenarios,
    "parameters" => parameters,
    )

    return outputGUI
end
